package exemplos;

public class exemplos {
    public static void main(String[] args) {

        byte parcela = 12;
        System.out.println("byte " + parcela);

        short idade = 18;
        System.out.println("Short " + idade);

        int cep = 84400000;
        System.out.println("int " + cep);

        long numeroCelular = 45999159055l;
        System.out.println("long " + numeroCelular);

        float valorTaxaMDR = 2.59f;
        System.out.println("float " + valorTaxaMDR);

        double valorJurosmes = 2.59d;
        System.out.println("double " + valorJurosmes);

        char letraA = 65;
        System.out.println("char " + letraA);

        boolean isUnderAge = false;
        System.out.println("boolean " + isUnderAge);

    }
}
