package poo.exemplos;

public class ExemplosPrimeiraAula {

    public static void main(String[] args) {
        byte caixaDeOvos = 12;
        System.out.println("byte: " + caixaDeOvos);

        short idade = 10;
        System.out.println("short: " + idade);

        int meuNumero = 449970007;
        System.out.println("int: " + meuNumero);

        float precoGenerico = 5.99f;
        System.out.println("float: " + precoGenerico);

        double precoSemF = 7.99;
        System.out.println("double: " + precoSemF);
    }
}
